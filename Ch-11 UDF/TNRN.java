
class TNRN {
    // TNRN (Take Nothing and Return Nothing)

    // Function
    public void greeting() {
        System.out.println("Welcome My Town...");
    }

    public void print() {
        System.out.println("Hello Jaynesh");
    }

    public static void main(String[] args) {
        TNRN t1 = new TNRN();

        t1.greeting(); // Calling
        t1.print();

    }
}